    public void showTBLstockBahan() throws SQLException{
        DefaultTableModel dtsb = new DefaultTableModel(new String[]{"Nama Bahan", "Stock", "Harga Satuan"},0);
        for(StockBahan sb : control.getDataBahan()){
            dtsb.addRow(new String[]{sb.getNama_bahan().toString(), sb.getStok().toString(), sb.getHarga_satuan().toString()});
        }
        TBLstockbahan.setModel(dtsb);
    }

