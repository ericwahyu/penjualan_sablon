    private void BTNinsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNinsertActionPerformed
        try {
            StockBahan SB = new StockBahan();
            String bahan = JOptionPane.showInputDialog("Nama Bahan");
            int stock = Integer.parseInt(JOptionPane.showInputDialog("Stock"));
            int harga = Integer.parseInt(JOptionPane.showInputDialog("Harga Satuan"));
            SB.setNama_bahan(bahan);
            SB.setStok(stock);
            SB.setHarga_satuan(harga);
            control.insertStockBahan(SB);
            showTBLstockBahan();
            bahan="";
            stock=0;
            harga=0;
        } catch (SQLException ex) {
            Logger.getLogger(GUI_stockBahan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BTNinsertActionPerformed

    private void BTNupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNupdateActionPerformed
        try {
            StockBahan stockBahan = control.getDataBahan().get(TBLstockbahan.getSelectedRow());
            String bahan = JOptionPane.showInputDialog("Nama Bahan");
            int stock = Integer.parseInt(JOptionPane.showInputDialog("Stock"));
            int harga = Integer.parseInt(JOptionPane.showInputDialog("Harga Satuan"));
            
            control.updateStockBahan(stockBahan, bahan, stock, harga);
            showTBLstockBahan();
        } catch (SQLException ex) {
            Logger.getLogger(GUI_stockBahan.class.getName()).log(Level.SEVERE, null, ex);
        }
            
            
    }//GEN-LAST:event_BTNupdateActionPerformed

    private void BTNdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNdeleteActionPerformed
        try {
            StockBahan stockBahan = control.getDataBahan().get(TBLstockbahan.getSelectedRow());
            control.deleteStockBahan(stockBahan);
            showTBLstockBahan();
        } catch (SQLException ex) {
            Logger.getLogger(GUI_stockBahan.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BTNdeleteActionPerformed

