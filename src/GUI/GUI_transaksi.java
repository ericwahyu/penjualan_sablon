    private void BTNtambahwarnaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNtambahwarnaActionPerformed
        try{
            DetailTransaksi dT = new DetailTransaksi();
            dT.setStockWarna(control.getDataWarna().get(CBwarna.getSelectedIndex()));
            dT.setJumlah(Integer.parseInt(TFjumlah.getText()));
            if(arrdetailtran.isEmpty()){
                
                    harga += control.getDataBahan().get(CBbahan.getSelectedIndex()).getHarga_satuan() * Integer.parseInt(TFjumlah.getText()) + control.getDataWarna().get(CBwarna.getSelectedIndex()).getHarga_warna() * Integer.parseInt(TFjumlah.getText());
                }else{
                    harga += control.getDataWarna().get(CBwarna.getSelectedIndex()).getHarga_warna() * Integer.parseInt(TFjumlah.getText());
                }  
            showHarga();
            arrdetailtran.add(dT);
            showTBwarna();
            //TFjumlah.setText("");
            CBbahan.disable();
            TFjumlah.disable();
        }catch(SQLException exx){
            
        }
    }//GEN-LAST:event_BTNtambahwarnaActionPerformed

    private void BTNmasuktransaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNmasuktransaksiActionPerformed
        CBbahan.enable();
        TFjumlah.enable();
        
        Transaksi tran = new Transaksi();
        try{
            tran.setTgl_transaksi(new SimpleDateFormat("dd/MM/yyy").parse(TFtgl.getText()));
            tran.setStockBahan(control.getDataBahan().get(CBbahan.getSelectedIndex()));
            tran.setTotal_harga(harga);
            tran.setJumlah_bahan(Integer.parseInt(TFjumlah.getText()));
            tran.setArrdetail(arrdetailtran);
            control.insertTransaksi(tran);
            showTBtransaksi();
            arrdetailtran = new ArrayList<>();
            showTBwarna();
            TFtgl.setText("");
            harga=0;
            showHarga();
            TFjumlah.setText("");
        } catch (ParseException ex) {
            Logger.getLogger(GUI_transaksi.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(GUI_transaksi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BTNmasuktransaksiActionPerformed

