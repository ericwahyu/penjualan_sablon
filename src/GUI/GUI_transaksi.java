    public void showCBbahan() throws SQLException{
        DefaultComboBoxModel dcbbahan = new DefaultComboBoxModel();
        for(StockBahan SB : control.getDataBahan()){
            dcbbahan.addElement(SB.getNama_bahan());
        }
       CBbahan.removeAllItems();
       CBbahan.setModel(dcbbahan);
    }
    
    public void showCBwarna()throws SQLException{
        DefaultComboBoxModel dcbwarna = new DefaultComboBoxModel();
        for(StockWarna SW : control.getDataWarna()){
                dcbwarna.addElement(SW.getWarna() + " (" + SW.getJeniswarna().getWarna_bahan()+")");           
        }
        CBwarna.removeAllItems();
        CBwarna.setModel(dcbwarna);
    }
    
    public void showTBwarna() throws SQLException{
        DefaultTableModel tbkeran = new DefaultTableModel(new String[]{
            "Warna", "Harga Warna", "Jenis Warna", "Jumlah Bahan"},0);
            for(DetailTransaksi dt : arrdetailtran){
                tbkeran.addRow(new String[]{dt.getStockWarna().getWarna().toString(), dt.getStockWarna().getHarga_warna().toString(), dt.getStockWarna().getJeniswarna().getWarna_bahan().toString(), dt.getJumlah().toString()
                });
            }
            TBLkeranjang.setModel(tbkeran);
    }
    
    public void showHarga(){
        TFharga.setText(harga.toString());
    }
    
    public void showTBtransaksi() throws SQLException{
        DefaultTableModel dtbtran = new DefaultTableModel(new String[]{"Bahan","Tanggal Transaksi","Total"},0);
        for(Transaksi T : control.getDataTransaksi()){
            dtbtran.addRow(new String[]{ T.getStockBahan().getNama_bahan(), T.getTgl_transaksi().toString(), T.getTotal_harga().toString()});
        }
        TBLtransaksi.setModel(dtbtran);
    }

