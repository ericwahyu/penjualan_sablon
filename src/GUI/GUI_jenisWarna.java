    public void showTBLjeniswarna() throws SQLException{
        DefaultTableModel dtjw = new DefaultTableModel(new String[]{"ID", "Warna Bahan"},0);
        for(JenisWarna jw : control.getDataJenisWarna()){
            dtjw.addRow(new String[]{jw.getId_jenisWarna().toString(), jw.getWarna_bahan().toString()});
        }
        TBLjeniswarna.setModel(dtjw);
    }

