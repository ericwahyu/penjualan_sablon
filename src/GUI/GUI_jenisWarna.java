    private void BTNinsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNinsertActionPerformed
        JenisWarna JW = new JenisWarna();
        try{
            String warna_bahan = JOptionPane.showInputDialog("Warna Bahan");
            JW.setWarna_bahan(warna_bahan);
            control.insertJenisWarna(JW);
            showTBLjeniswarna();
            
        } catch (SQLException ex) {
            Logger.getLogger(GUI_jenisWarna.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BTNinsertActionPerformed

